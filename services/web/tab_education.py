def _section_theory() -> None:
    """Краткая теория парольной безопасности."""
    st.subheader("Основы парольной безопасности")
    st.write("""
    **Надежность пароля** — это его устойчивость к различным методам подбора:
    атакам по словарю, перебору на основе утечек, статистическим моделям
    (как наша Марковская модель), полному перебору.

    **Энтропия пароля** — логарифмическая мера разнообразия. Измеряется в
    битах. Пароль с энтропией N бит имеет 2ᴺ возможных вариантов. Для
    современных требований минимально достаточно ~50 бит, рекомендуется ≥70 бит.

    **Основные угрозы:**
    - **Атака по словарю** — перебор паролей из готового списка
      (распространенные слова, имена, даты). Очень быстрая.
    - **Атака по утечкам (credential stuffing)** — использование пароля,
      утекшего на одном сайте, для входа на другой.
    - **Брутфорс (полный перебор)** — последовательный перебор всех возможных
      комбинаций. Долгий, но неизбежно успешный при коротких паролях.
    - **Гибридные атаки** — комбинация словаря с правилами модификации:
      замена `o→0`, добавление цифр, leet-speak. Именно их моделирует Hashcat
      на основе Марковских моделей, аналогичных нашей.

    **Чем хороший пароль отличается от плохого:**
    - Длина важнее сложности. 16 строчных букв безопаснее 8 «сложных».
    - Не должен встречаться в утечках (HIBP, RockYou).
    - Не должен быть основан на словарных словах, личной информации,
      клавиатурных паттернах.
    - Не должен переиспользоваться между сервисами.
    """)


def _section_xkcd() -> None:
    """Парадокс XKCD с интерактивной демонстрацией."""
    st.subheader("Парадокс XKCD: длина важнее сложности")

    # Показываем картинку XKCD если она доступна.
    if Path(XKCD_IMAGE_PATH).exists():
        st.image(
            XKCD_IMAGE_PATH,
            caption=(
                "Парадокс требований к паролям. Источник: Munroe R., "
                "Password Strength, xkcd #936, CC BY-NC 2.5"
            ),
            use_column_width=True,
        )
    else:
        st.caption(
            "Иллюстрация комикса XKCD #936 («Password Strength») недоступна. "
            "Полное описание парадокса приведено ниже."
        )

    st.write("""
    Известный комикс [XKCD #936](https://xkcd.com/936/) (Рэндалл Манро, 2011)
    наглядно показал противоречие в типичных требованиях к паролям:
    «сложный» пароль вроде `Tr0ub4dor&3` обладает энтропией ~28 бит,
    а простая парольная фраза `correct-horse-battery-staple` — ~44 бит.

    Это привело к пересмотру международных рекомендаций:
    - **NIST SP 800-63B** (ред. 2017) убрал требования к сложности и
      обязательной периодической смене паролей, сделав упор на длину
      и проверку по базам утечек.
    - **EFF Diceware** (используется в нашем генераторе) популяризовал
      подход к парольным фразам как стандарту.

    **Проверьте сами:**
    """)

    col1, col2 = st.columns(2)
    with col1:
        if st.button(
            "Проверить `Tr0ub4dor&3`",
            use_container_width=True,
            key="xkcd_btn_1",
        ):
            try:
                st.session_state["xkcd_result_1"] = api_client.check("Tr0ub4dor&3")
            except httpx.HTTPError as e:
                st.error(f"Ошибка: {e}")

    with col2:
        if st.button(
            "Проверить `correct-horse-battery-staple`",
            use_container_width=True,
            key="xkcd_btn_2",
        ):
            try:
                st.session_state["xkcd_result_2"] = api_client.check(
                    "correct-horse-battery-staple"
                )
            except httpx.HTTPError as e:
                st.error(f"Ошибка: {e}")

    # Показываем результаты в две колонки бок о бок.
    r1 = st.session_state.get("xkcd_result_1")
    r2 = st.session_state.get("xkcd_result_2")

    if r1 or r2:
        col1, col2 = st.columns(2)
        with col1:
            if r1:
                st.write(f"**`Tr0ub4dor&3`** — {r1['verdict']}")
                st.caption(
                    f"zxcvbn: {r1['zxcvbn']['score']}/4 · "
                    f"взлом: {r1['zxcvbn']['crack_time']}"
                )
                st.caption(f"Markov: {r1['markov']['score']:.2f}")
                st.caption(f"Утечка: {'найден' if r1['breach']['found'] else 'не найден'}")
        with col2:
            if r2:
                st.write(f"**`correct-horse-battery-staple`** — {r2['verdict']}")
                st.caption(
                    f"zxcvbn: {r2['zxcvbn']['score']}/4 · "
                    f"взлом: {r2['zxcvbn']['crack_time']}"
                )
                st.caption(f"Markov: {r2['markov']['score']:.2f}")
                st.caption(f"Утечка: {'найден' if r2['breach']['found'] else 'не найден'}")


def _section_worst_passwords() -> None:
    """Топ-10 худших паролей с проверкой через API."""
    st.subheader("Топ-10 наиболее распространенных утекших паролей")
    st.write(
        "По данным анализа множества публичных утечек, эти пароли используются "
        "наибольшим числом людей. **Гарантированно скомпрометированы.**"
    )

    if st.button("Проверить все 10 паролей", key="check_top10"):
        with st.spinner("Проверяю..."):
            results = []
            for pwd in TOP_10_WORST:
                try:
                    r = api_client.check(pwd)
                    results.append((pwd, r))
                except httpx.HTTPError:
                    results.append((pwd, None))
            st.session_state["top10_results"] = results

    if "top10_results" in st.session_state:
        for i, (pwd, r) in enumerate(st.session_state["top10_results"], 1):
            if r is None:
                st.write(f"{i}. `{pwd}` — ошибка проверки")
                continue
            verdict = r["verdict"]
            in_breach = "✅" if r["breach"]["found"] else "❌"
            crack = r["zxcvbn"]["crack_time"]
            markov = r["markov"]["score"]
            st.write(
                f"{i}. `{pwd}` — {verdict}\n"
                f"   • В утечке: {in_breach}  "
                f"• Время взлома: {crack}  "
                f"• Markov: {markov:.2f}"
            )


def _section_mistakes() -> None:
    """Типичные ошибки при создании паролей."""
    st.subheader("Типичные ошибки при создании паролей")

    st.write("""
    **Чего следует избегать:**

    1. **Словарные слова и имена** — `password`, `dragon`, `michael`, `jordan`.
       Любые слова из любых языков подбираются по словарю мгновенно.

    2. **Клавиатурные паттерны** — `qwerty`, `qazwsx`, `1qaz2wsx`, `asdf1234`.
       Hashcat и аналогичные инструменты ловят эти шаблоны в первую очередь.

    3. **Личная информация** — даты рождения, имена детей, клички питомцев,
       названия любимых команд. Доступна злоумышленнику из соцсетей.

    4. **Простые модификации** — `password1`, `Password!`, `P@ssw0rd`.
       Это и есть «парадокс XKCD»: формально соответствует требованиям,
       но взламывается за секунды. **Leet-speak подбирается тривиально.**

    5. **Повторяющиеся последовательности** — `111111`, `aaaaaa`, `123123`.

    6. **Переиспользование между сервисами** — даже сильный пароль становится
       слабым, если он один на все сайты. Утечка одного = взлом всех.

    **Что делать вместо этого:**

    - Используйте **менеджер паролей** (Bitwarden, KeePass, 1Password).
    - Для каждого сервиса — **уникальный** пароль, сгенерированный криптогенератором.
    - Главный пароль от менеджера — **парольная фраза** из 4-6 случайных слов.
    - Включайте **двухфакторную аутентификацию** где возможно — это снижает
      критичность даже компрометации пароля.
    """)


